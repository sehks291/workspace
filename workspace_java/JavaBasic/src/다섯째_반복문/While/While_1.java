package 다섯째_반복문.While;

/*
*  While(반복 조건){
*  반복적으로 실행되는 내용
*  }
* */

import java.util.Scanner;

public class While_1 {
    public static void main(String[] args) {

        // while문 필수 3요소
        // @1 반복 시작점
        // @2 반복 조건
        // @3 반복 파기 조건
        int i = 1; // @1

        while(i <= 3){ // @2
                System.out.println(1);
                i++; //@3
            }
        }
    }
