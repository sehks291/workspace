package 변수와자료형;

public class 숫자연산 {
    public static void main(String[] args) {

        System.out.println(10+10);

        // 정수끼리의 연산 결과는 정수
        System.out.println(10/4);
        // 실수끼리의 연산 결과는 실수
        System.out.println(10.0/4.0);
        // 정수, 실수끼리의 연산 결과는 실수
        System.out.println(10.0/4);
        System.out.println(10/4.0);
    }
}
